export function getPromt(name = 'Герман') {
  let promt = milenaIdentity;
  const varibles = [{ name: 'NAME', value: name }];
  varibles.forEach(({ name, value }) => {
    promt = promt.replace(new RegExp(`{${name}}`, 'g'), value);
  });

  return promt;
}
